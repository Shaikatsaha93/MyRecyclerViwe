package com.example.diu.myrecyclerviwe;

public class Listitem {

    private String name;
    private String email;

    public Listitem(String name, String email) {
        this.name = name;
        this.email = email;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }
}
